	<p>
		Результативность научной деятельности профессорско-преподавательского состава складывается исходя из индивидуальных критериев результативности
		научной деятельности (КРНД) ученых СКФ БГТУ им.В.Г.Шухова. КРНД выражается суммой баллов, определяемых в соответствии с утвержденным положением.
	</p>
	<p>
		К приоритетным оценочным критериям относятся:
	</p>
	<ul type="disc">
		<li>публикации в рецензируемых периодических российских и зарубежных журналах, имеющих импакт-фактор;</li>
		<li>издание монографий, учебников и учебных пособий, в том числе с грифом Министерства;</li>
		<li>получение патента на изобретение, полезную модель, свидетельства о регистрации баз данных, программ для ЭВМ (Роспатент), свидетельства об
			отраслевой регистрации разработки (ОФАП);
		</li>
		<li>выполнение проектов поддержанных грантами различных научных фондов и научных программ, выполнение работ по хоздоговорной тематике, участие в
			конкурсе научных работ или научных выставках с получением медали, грамоты, диплома победителя и т.д.
		</li>
	</ul>

