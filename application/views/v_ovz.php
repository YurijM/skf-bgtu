		ежегодно проводятся культурно-воспитательные мероприятия, такие как: лектории; конкурсы; спортивные
		состязания; встречи с интересными людьми, ветеранами и участниками боевых действий, представителями
		профильных служб: миграционной службы, полиции, УФСКН, медицины, духовенства и др. Студенты выезжают на
