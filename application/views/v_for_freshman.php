        <strong>Учебный процесс</strong>
        <p>
          <em><strong>Учебный год</strong></em> в университете для студентов начинается 1 сентября и заканчивается согласно учебному плану. Учебный год состоит из двух семестров. Занятия проходят в соответствии с графиком учебного процесса и расписанием занятий, которое расположено на доске объявлений и на сайте.
        </p>
        <p>
          <em><strong>Сессии</strong></em> проходят 2 раза в год: летняя и зимняя.
        </p>
        <p>
          Экзаменационной сессии предшествует зачетная неделя.
        </p>
        <p>
          При наличии уважительных причин и удостоверяющих это документов сессия может быть сдана досрочно или продлена. В случае выявленной в ходе промежуточной аттестации низкой успеваемости и посещаемости отдельных студентов 1-4 курсов куратор уведомляет об этом их родителей и других родственников.
        </p>
        <p>
          В случае отчисления студента, обучающегося с полным возмещением затрат, деньги за обучение не возвращаются, если образовательные услуги уже были оказаны.
        </p>
        <p>
          В случае ухода в академический отпуск за студентом бюджетной формы обучения сохраняется бюджетное место, в случае его отчисления из-за академической неуспеваемости, он теряет бюджетное место.
        </p>
        <p>
          Учебные занятия проводятся в виде лекций, консультаций, семинаров, практических занятий, лабораторных, контрольных, самостоятельных работ, коллоквиумов, научно-исследовательской работы студентов, практик, курсового проектирования (курсовой работы).
          Для всех видов аудиторных учебных занятий академический час устанавливается продолжительностью 45 минут. Одно занятие включает, как правило, два академических часа.
        </p>
        <p>
          Качество освоения образовательных программ оценивается в университете путем осуществления текущего контроля успеваемости, промежуточной аттестации обучающихся и итоговой аттестации выпускников.
        </p>
        <p>
          В университете проводятся различные формы текущей аттестации: устные опросы, контрольные работы, тестирование, коллоквиумы и др.
        </p>
        <p>
          Промежуточная аттестация осуществляется в форме экзаменов и зачетов, проводимых после выполнения обучающимися всех планируемых в семестре видов занятий.
        </p>
        <p>
          В процессе обучения успеваемость обучающихся (знания, умения и навыки) определяются на экзаменах оценками "отлично", "хорошо", "удовлетворительно", "неудовлетворительно".
        </p>
        <p>
          Для дисциплин и видов работы, по которым формой текущего либо промежуточного контроля является зачет, устанавливаются оценки "зачтено" и "не зачтено".
        </p>
        <p>
          Кроме основных образовательных программ (бакалавриат, специалитет) филиал осуществляет на возмездной основе набор на дополнительные образовательные программы на курсах повышения квалификации и профессиональной переподготовке в объеме 72 - 520 часов.
