        <strong>Основные понятия</strong>
        <p>
          <em><strong>Университет</strong></em> (от лат. совокупность) - многопрофильное высшее учебно-научное заведение, ведущее подготовку высококвалифицированных кадров по широкому кругу специальностей, гуманитарных, технических, естественных наук.
        </p>
        <p>
          <em><strong>Ректор</strong></em> (от лат. управитель) - руководитель высшего учебного заведения.
        </p>
        <p>
          <em><strong>Директор</strong></em> (от лат. управлять) — управляющий, руководитель компании, предприятия или учебного заведения.
        </p>
        <p>
          <em><strong>Профессор</strong></em> (от лат. преподаватель, учитель) - ученое звание и должность преподавателя высшего учебного заведения.
        </p>
        <p>
          <em><strong>Доцент</strong></em> (от лат. обучающий) - ученое звание и должность преподавателей вузов, имеющих степень кандидата наук, определенный стаж работы, научные труды и изобретения.
        </p>
        <p>
          <em><strong>Старший преподаватель</strong></em> - преподаватель, ведущий учебную, научно-исследовательскую работу под руководством профессора или доцента.
        </p>
        <p>
          <em><strong>Ассистент</strong></em> - преподаватель, ведущий учебную, научно-исследовательскую работу под руководством профессора или доцента.
        </p>
        <p>
          <em><strong>Семестр</strong></em> - половина учебного года в университете (1-й семестр, 2-й семестр).
        </p>
        <p>
          <em><strong>Экзаменационная сессия</strong></em> - период сдачи студентами экзаменов по окончании каждого семестра в строго определенное время.
        </p>
        <p>
          <em><strong>Учебный план</strong></em> - документ, определяющий, какие учебные предметы, в каком семестре изучаются, какое количество часов отводится на их изучение (виды занятий по каждой дисциплине, лекции, практические занятия) и виды аттестации. Ознакомиться с учебным планом можно в деканате.
        </p>
        <p>
          <em><strong>Учебная программа</strong></em> - документ, определяющий по каждому предмету цель, содержание и объем знаний, умений и навыков, подлежащих усвоению.
        </p>
        <p>
          <em><strong>Академический отпуск</strong></em> - это отпуск, предоставляемый студентам по их заявлениям, не имеющим возможности временно продолжить обучение по медицинским показаниям (согласно заключения клинико-экспертной комиссии) и в других исключительных случаях (стихийные бедствия, семейные обстоятельства и др.), подтвержденных документально. Академический отпуск предоставляется один раз за весь период обучения в университете. Академический отпуск по медицинским показаниям предоставляется на 12 календарных месяцев.
        </p>
        <p>
          <em><strong>Диплом</strong></em> - документ государственного образца, выдаваемый студентам, прошедшим итоговую аттестацию. Студентам, имеющим оценку "отлично" не менее чем по 75% всех учебных дисциплин, а по остальным - оценку "хорошо" и прошедшим итоговую аттестацию с оценкой "отлично", выдается диплом с отличием.
        </p>
        <p>
          <em><strong>Зачетная неделя</strong></em> проводится после окончания чтения лекций и выполнения лабораторных и практических работ, до начала экзаменационной сессии. В этот период студент должен ликвидировать все задолженности (контрольные, курсовые работы и др.) и сдать все установленные зачеты.
        </p>
