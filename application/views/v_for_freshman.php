        <strong>Научная жизнь</strong>
        <p>
          <em><strong>Университет</strong></em> - это учебное заведение, где студент не только получает специальность, но и приобщается к исследовательской деятельности. Для этого в университете есть необходимые условия.
        </p>
        <p>
          Каждая кафедра университета является центром научных исследований в своей области. Каждый преподаватель ведет научную работу.
        </p>
        <p>
          Первые рефераты, творческие, расчетные, проектные задания, курсовая работа - это начало приобщения к научной деятельности.
          В университете создано и работает <em><strong>студенческое научное общество (СНО)</strong></em>, при каждой кафедре действует совет СНО, который возглавляет председатель из студенческого коллектива.
        </p>
        <p>
          Ежегодно СНО проводит студенческие научные конференции. Работа в них ведется в секциях по специальным направлениям.
          Лучшие работы студентов представляются на университетскую студенческую конференцию, Всероссийский открытый конкурс научных работ по естественным, техническим и гуманитарным дисциплинам, преподаваемым в вузах Российской Федерации.
        </p>
        <p>
          Студенты, успешно занимающиеся научной работой, могут представлять результаты своих исследований для публикаций, в том числе в изданиях университета.
