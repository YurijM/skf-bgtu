      <div class="info bg-info text-info">
        <strong>Как записывать лекцию</strong>
        <p>
          Лекция - это важный источник информации по каждой учебной дисциплине. Она ориентирует студента в основных проблемах изучаемого курса, направляет самостоятельную работу над ним.
        </p>
        <p>
          Запись лекций по каждому предмету ведется в отдельной тетради. Прежде всего, запишите фамилию, имя, отчество преподавателя-лектора, оставьте первые страницы для записи названий рекомендуемых учебников, пособий, хрестоматий, справочников, сборников задач, упражнений, дополнительной литературы.
        </p>
        <p>
          Будьте внимательны, когда лектор называет тему лекции - запишите ее. Целесообразно записать план лекции. Не забудьте оставить поля - они понадобятся для различных пометок, замечаний, вопросов.
        </p>
        <p>
          Запись содержания лекции индивидуальна, именно поэтому трудно пользоваться чужими конспектами лекций.
        </p>
      </div>

      <div class="info bg-success text-success">
        <strong>Как готовиться к практическому занятию (семинару)</strong>
        <p>
          С первых дней обучения в вузе вы увидите, что лекции чередуются в расписании с практическими занятиями. Цель этих занятий - углубленное изучение той или иной дисциплины.
        </p>
        <p>
          Практические занятия - это общее понятие для обозначения практикумов, лабораторных, семинарских занятий. Практические занятия соответствуют главным целям, идеям, основному содержанию, логике лекционного курса. Само определение "практические" указывает на их главную цель: связь теории и практики.
        </p>
        <p>
          Из всех видов практических занятий остановимся на подготовке к семинару.
        </p>
        <p>
          Как правило, к семинарскому занятию преподаватель сообщает студентам тему, план, основную и дополнительную литературу, выделяет проблемы для коллективного обсуждения, определяет формы участия студентов в семинаре (докладчики, содокладчики, оппоненты и т.д.). Объясняет, как работать с источниками.
        </p>
        <p>
          Готовясь к семинару, целесообразно сделать следующее:
        </p>
        <ol>
          <li>Просмотреть записи лекций по теме.</li>
          <li>Прочитать соответствующую главу из учебного пособия, рекомендованного преподавателем.</li>
          <li>Составить конспект по источникам, в которых рассматриваются вопросы темы семинара.</li>
          <li>Обязательными условиями проведения семинара являются развернутое выступление самих студентов по основным вопросам изучаемой темы, обмен мнениями, полемика, диалог. Поэтому вы должны подготовиться:
            <ul type="disc">
              <li>к выступлению по отдельному вопросу с небольшим докладом или сообщением;</li>
              <li>к участию в коллективном обсуждении проблемы с обоснованием своей точки зрения, своей позиции.</li>
            </ul>
          </li>
        </ol>
        По итогам проведенного семинара преподаватель может выставлять оценки, которые учитываются при аттестации (зачете, экзамене).
