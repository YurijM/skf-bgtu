      <div class="info bg-info text-info">
        <strong>Как готовиться к традиционному зачету</strong>
        <ol>
          <li>Внимательно отнеситесь к сообщению преподавателя (обычно это делается в начале изучения курса) о сроках сдачи зачета, о его содержании, форме проведения и о тех требованиях, которым должен соответствовать ответ студента на зачете.</li>
          <li>Уже в начале изучения курса вы должны выяснить, есть ли на соответствующей кафедре перечень тем и вопросов, по которым будет проводиться зачет, и скопировать их.</li>
          <li>Вы должны четко знать круг источников, которые потребуются для подготовки к зачету.</li>
          <li>Основной способ подготовки к зачету - систематическая самостоятельная работа над курсом, регулярная подготовка и посещение практических занятий.</li>
          <li>Своевременно восстанавливайте пробелы в знаниях, возникающие вследствие болезни, непонимания, отсутствия необходимых пособий.</li>
          <li>Выясняйте непонятное в содержании (определения, проблемы, концепции, теории, способы решения задач) у преподавателя или у однокурсников. Но при этом всегда помните, что высшая школа предполагает постоянную самостоятельную работу самого студента.</li>
        </ol>
      </div>

      <div class="info bg-success text-success">
        <strong>Как готовиться к экзамену</strong>
        <p>
          Экзамен (от лат. ехатеп - взвешивание, испытание) - одна из основных форм проверки знаний, умений и навыков студентов в результате изучения всей дисциплины или ее части.
        </p>
        <p>
          Экзамены в высшей школе сдаются два раза в год: в летнюю и зимнюю сессии, проводятся устно или письменно по билетам, тестам, вариантам различных заданий.
        </p>
        <p>
          <strong>Помните</strong>: одинакового для всех способа подготовки к экзаменам не существует. Это зависит от ваших индивидуальных особенностей, от содержания предмета, наличия текстов лекций, учебных пособий. Однако есть ряд правил, которые можно соблюдать при подготовке к экзамену:
        </p>
        <ol>
          <li>Необходимо иметь программу курса и вопросы.</li>
          <li>Ориентировочной основой подготовки к экзамену являются конспекты лекций, материалы практических занятий, конспекты
первоисточников, которые помогут вам восстановить содержание, логику предмета.</li>
          <li>В лекциях не все вопросы курса раскрываются достаточно полно, поэтому необходимо иметь при подготовке к экзамену учебные пособия, хрестоматии, сборники задач, методические рекомендации.</li>
          <li>Распределяйте учебный материал по дням, оставив последний день для систематизации и обобщения знаний. Анализируя вопросы, выделите те, которые требуют особого внимания и большего времени на подготовку.</li>
          <li>Записывайте то, что вам непонятно, чтобы на консультации задать вопросы.</li>
          <li>Чтобы лучше освоить содержание курса, при подготовке делайте краткие записи изученного материала, чертите схемы, записывайте факты, названия. Используйте систему условных обозначений, символов и т.д. В любом вопросе выделите самое главное, мысленно представьте план своего ответа.</li>
        </ol>
