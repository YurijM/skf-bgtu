	</p>

	<h6>
		Статья 1. п.1
	</h6>
	<p>
		<b>Коррупция:</b> злоупотребление служебным положением, дача взятки, получение взятки, злоупотребление полномочиями, коммерческий подкуп либо иное
		незаконное использование физическим лицом своего должностного положения вопреки законным интересам общества и государства в целях получения выгоды в
		виде денег, ценностей, иного имущества или услуг имущественного характера, иных имущественных прав для себя или для третьих лиц либо незаконное
		предоставление такой выгоды указанному лицу другими физическими лицами.
	</p>

	<h6>
		Статья 1. п.2
	</h6>
	<p>
		<b>Противодействие коррупции:</b> деятельность федеральных органов государственной власти, органов государственной власти субъектов Российской
		Федерации, органов местного самоуправления, институтов гражданского общества, организаций и физических лиц в пределах их полномочий:
	</p>

	<ul style="list-style-type: disc">
		<li>
			по предупреждению коррупции, в том числе по выявлению и последующему устранению причин коррупции (профилактика коррупции);
		</li>
		<li>
			по выявлению, предупреждению, пресечению, раскрытию и расследованию коррупционных правонарушений (борьба с коррупцией);
		</li>
		<li>
			по минимизации и (или) ликвидации последствий коррупционных правонарушений.
		</li>
	</ul>

